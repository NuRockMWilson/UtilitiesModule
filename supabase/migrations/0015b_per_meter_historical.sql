insert into utility_accounts (
  property_id, vendor_id, gl_account_id, account_number, meter_id, description,
  is_house_meter, is_vacant_unit, is_clubhouse, active
)
select
  p.id, v.id, g.id, s.account_number, nullif(s.meter_id, ''), s.description,
  s.is_house_meter, s.is_vacant_unit, s.is_clubhouse, true
from seed s
join properties  p on p.code        = s.property_code
join vendors     v on v.short_name  = s.vendor_short
join gl_accounts g on g.code        = s.gl_code
on conflict (vendor_id, account_number) do nothing;
